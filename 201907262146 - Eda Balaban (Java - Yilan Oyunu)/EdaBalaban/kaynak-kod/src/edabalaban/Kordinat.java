package edabalaban;

public class Kordinat {
	public int x;
	public int y;
	public int xf;
	public int yf;
	
	public Kordinat(int x, int y) {
		this.x = x;
		this.y = y;
	}
	public void Degistir(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getXf() {
		return xf;
	}

	public int getYf() {
		return yf;
	}

	

}
